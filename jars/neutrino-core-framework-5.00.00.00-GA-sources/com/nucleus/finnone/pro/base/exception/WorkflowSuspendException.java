package com.nucleus.finnone.pro.base.exception;

public class WorkflowSuspendException extends BaseException {

    public WorkflowSuspendException() {
        super();
    }

    public WorkflowSuspendException(String message) {
        super(message);
    }

    public WorkflowSuspendException(String message, Throwable cause) {
        super(message, cause);
    }

    public WorkflowSuspendException(Throwable cause) {
        super(cause);
    }

}
